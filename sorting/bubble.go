package sorting

// bubble iterates through the array multiple times, swapping adjacent elements if they are in the wrong order
func bubble(arr []int) []int {
	n := len(arr)
	for i := 0; i < n-1; i++ {
		for j := 0; j < n-i-1; j++ {
			// swap with next element if current element is greater
			if arr[j] > arr[j+1] {
				arr[j], arr[j+1] = arr[j+1], arr[j]
			}
		}
	}
	return arr
}
